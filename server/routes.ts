async function fireAutoMessageTriggers(applicationId: string, newStatus: string) {
  try {
    const app = await storage.getApplication(applicationId);
    if (!app || !app.packageId) return;

    const triggers = await storage.getAutoMessageTriggers(app.packageId);
    const matchingTriggers = triggers.filter(t => t.triggerStatus === newStatus);

    for (const trigger of matchingTriggers) {
      let recipientId: string | null = null;
      if (trigger.recipientType === "patient" && app.userId) {
        recipientId = app.userId;
      } else if (trigger.recipientType === "doctor") {
        recipientId = app.assignedReviewerId || app.assignedAgentId || null;
      }

      if (recipientId) {
        const messageBody = trigger.messageTemplate
          .replace("{{applicationId}}", applicationId)
          .replace("{{status}}", newStatus)
          .replace("{{packageName}}", app.packageName || "your service");

        await storage.createMessage({
          senderId: "system",
          receiverId: recipientId,
          subject: `Application Update: ${newStatus}`,
          content: messageBody,
          isRead: false,
          isSystemMessage: true,
        });

        await storage.createNotification({
          userId: recipientId,
          type: "auto_message",
          title: `Application Update`,
          message: messageBody.substring(0, 200),
          isRead: false,
        });
      }
    }
  } catch (error) {
    console.error("Error firing auto-message triggers:", error);
  }
}

function fillFormTemplate(template: string, placeholders: Record<string, string>): string {
  let filled = template;
  for (const [key, value] of Object.entries(placeholders)) {
    const regex = new RegExp(`\\{\\{\\s*${key}\\s*\\}\\}`, 'gi');
    filled = filled.replace(regex, value || '');
  }
  return filled;
}

async function autoGenerateDocument(applicationId: string, doctorId: string) {
  try {
    const app = await storage.getApplication(applicationId);
    if (!app) return;

    const doctorProfile = await storage.getDoctorProfileByUserId(doctorId);
    const patient = app.userId ? await storage.getUser(app.userId) : null;
    const formData = app.formData || {};

    const placeholders: Record<string, string> = {
      doctorName: doctorProfile?.fullName || "Physician",
      doctorLicense: doctorProfile?.licenseNumber || "",
      doctorNPI: doctorProfile?.npiNumber || "",
      doctorDEA: doctorProfile?.deaNumber || "",
      doctorPhone: doctorProfile?.phone || "",
      doctorFax: doctorProfile?.fax || "",
      doctorAddress: doctorProfile?.address || "",
      doctorSpecialty: doctorProfile?.specialty || "",
      doctorState: doctorProfile?.state || "",
      patientName: patient ? `${patient.firstName || ''} ${patient.lastName || ''}`.trim() : "Patient",
      patientFirstName: patient?.firstName || "",
      patientLastName: patient?.lastName || "",
      patientMiddleName: patient?.middleName || "",
      patientEmail: patient?.email || "",
      patientPhone: patient?.phone || "",
      patientDOB: patient?.dateOfBirth || formData.dateOfBirth || "",
      patientAddress: patient?.address || formData.address || "",
      patientCity: patient?.city || formData.city || "",
      patientState: patient?.state || formData.state || "",
      patientZipCode: patient?.zipCode || formData.zipCode || "",
      patientSSN: patient?.ssn || formData.ssn || "",
      patientDriverLicense: patient?.driverLicenseNumber || formData.driverLicenseNumber || "",
      patientMedicalCondition: patient?.medicalCondition || formData.medicalCondition || "",
      packageName: app.packageName || "Service Document",
      applicationId,
      date: new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }),
      dateShort: new Date().toLocaleDateString('en-US'),
      reason: formData.reason || formData.notes || "",
    };

    if (formData && typeof formData === 'object') {
      for (const [key, value] of Object.entries(formData)) {
        if (typeof value === 'string' || typeof value === 'number') {
          placeholders[key] = String(value);
        }
      }
    }

    let generatedHtml = "";
    if (doctorProfile?.formTemplate) {
      generatedHtml = fillFormTemplate(doctorProfile.formTemplate, placeholders);
    }

    const docContent = {
      applicationId,
      packageName: app.packageName || "Service Document",
      patientName: placeholders.patientName,
      patientEmail: placeholders.patientEmail,
      doctorName: placeholders.doctorName,
      doctorLicense: placeholders.doctorLicense,
      doctorNPI: placeholders.doctorNPI,
      doctorDEA: placeholders.doctorDEA,
      generatedAt: new Date().toISOString(),
      status: "auto_generated",
      notes: app.level2Notes || app.level3Notes || "",
      generatedHtml,
    };

    const document = await storage.createDocument({
      applicationId,
      userId: app.userId || "",
      name: `${app.packageName || "Document"} - Auto Generated`,
      type: "auto_generated",
      status: "completed",
      fileUrl: "",
      metadata: JSON.stringify(docContent),
    });

    return document;
  } catch (error) {
    console.error("Error auto-generating document:", error);
  }
}

const memoryUpload = multer({
  storage: multer.memoryStorage(),
  limits: { fileSize: 25 * 1024 * 1024 },
  fileFilter: (_req, file, cb) => {
    const allowed = ["image/jpeg", "image/png", "image/gif", "image/webp", "image/svg+xml", "video/mp4", "video/webm"];
    if (allowed.includes(file.mimetype)) {
      cb(null, true);
    } else {
      cb(new Error("Only image files (JPEG, PNG, GIF, WebP, SVG) and short videos (MP4, WebM) are allowed"));
    }
  },
});

const documentUpload = multer({
  storage: multer.memoryStorage(),
  limits: { fileSize: 20 * 1024 * 1024 },
  fileFilter: (_req, file, cb) => {
    if (file.mimetype === "application/pdf") {
      cb(null, true);
    } else {
      cb(new Error("Only PDF files are allowed"));
    }
  },
});

